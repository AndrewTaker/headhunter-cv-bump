<h1>Сервис для автоматического поднятия резюме на <a href="https://headhunter.ru">HeadHunter</a></h1>
<p>Подписка <a href="https://hh.ru/applicant-services/hhpro">HHPro</a> даёт возможность поднимать резюме автоматически каждые четыре часа. Сервис позволяет делать это без подписки.</p>
<hr />

<h2>Какие данные хранятся</h2>
<p>Используется авторизация через <a href="https://oauth.net/2/">Oauth2</a>, что даёт сервису доступ к информации пользователя, какой её видит HeadHunter.ru</p>
<ul>
    <li>Идентификатор пользователя, имя, фамилия и отчество</li>
    <li>Идентификатор резюме пользователя, название, дата создания и дата обновления резюме</li>
    <li>Зашифрованные персональные <a href="https://api.hh.ru/openapi/redoc#section/Avtorizaciya/Poluchenie-access-i-refresh-tokenov">токены</a></li>
</ul>
